  GJ.jobsDataToBbString = function (chosenJobName) {
    chosenJobName = chosenJobName.toLowerCase();
    var str = '';
    var jobs = Map.JobHandler.Featured;
    var k,
    jobId,
    job,
    color,
    j,
    l,
    c = [];
    for (k in jobs) {
      var jobPlace = jobs[k];
      for (jobId in jobPlace) {
        job = JobList.getJobById(jobId);
        if (chosenJobName !== '' && job.name.toLowerCase().indexOf(chosenJobName) < 0)
          continue;
        j = jobPlace[jobId];
        color = j.gold ? GJ.goldJobBbColor : GJ.silverJobBbColor;
        c.push('[b][color=' + color + ']' + job.name + '[/color][/b] (' + j.x + ';' + j.y + ')\n');
      }
    }
    c.sort(function (a, b) {
      a = a.replace(new RegExp(GJ.goldJobBbColor, 'g'), '');
      a = a.replace(new RegExp(GJ.silverJobBbColor, 'g'), '');
      b = b.replace(new RegExp(GJ.goldJobBbColor, 'g'), '');
      b = b.replace(new RegExp(GJ.silverJobBbColor, 'g'), '');
      return (a == b) ? 0 : (a > b) ? 1 :  - 1;
    });
    for (l = 0; l < c.length; l++)
      str += c[l];
    return str;
  };
